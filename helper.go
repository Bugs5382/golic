import (
	"os"
	"path/filepath"
)

func GetProjectRoot() string {
	dir, err := os.Getwd()
	if err != nil {
		return "."
	}
	for {
		if _, err := os.Stat(filepath.Join(dir, "go.mod")); err == nil {
			return dir
		}
		parent := filepath.Dir(dir)
		if parent == dir { // Reached the system root
			break
		}
		dir = parent
	}
	return "."
}
